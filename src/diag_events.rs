// Event ID lookup tables derived from the Wireshark S7Comm dissector
// (packet-s7comm.c, seemoo-lab/nexmon). Arrays are sorted by key so
// binary_search_by_key gives O(log n) lookup with zero allocations.

// Lookup helper shared by all three tables.
fn lookup(table: &[(u16, &'static str)], id: u16) -> Option<&'static str> {
    table
        .binary_search_by_key(&id, |&(k, _)| k)
        .ok()
        .map(|i| table[i].1)
}

/// Human-readable description of a [`DiagnosticEntry`](crate::DiagnosticEntry) event ID.
///
/// Obtain via [`describe_event`].
pub struct DiagEventInfo {
    /// Event class derived from the high nibble (bits 12–15), e.g. `"Mode transitions"`.
    pub class: &'static str,
    /// Specific event name from the S7 diagnostic tables, or `None` if not catalogued.
    pub name: Option<&'static str>,
}

/// Returns a [`DiagEventInfo`] for the given raw `event_id` from a diagnostic buffer entry.
///
/// Lookup strategy (mirrors the Wireshark S7Comm dissector):
/// - The high nibble (bits 12–15) always identifies the event class.
/// - IDs with class `0x8` or `0x9` are looked up in the standardised diagnostic /
///   predefined-user-event table; all others use the fixed-event table.
///
/// # Example
///
/// ```
/// use rust7::describe_event;
///
/// let info = describe_event(0x4302);
/// assert_eq!(info.class, "Mode transitions");
/// assert_eq!(info.name, Some("Mode transition from STARTUP to RUN"));
/// ```
pub fn describe_event(event_id: u16) -> DiagEventInfo {
    let class_nibble = event_id >> 12;
    let class = lookup(EVENT_CLASS_NAMES, class_nibble).unwrap_or("Unknown");
    let name = if class_nibble == 0x8 || class_nibble == 0x9 {
        lookup(EVENT_0X8_0X9_NAMES, event_id)
    } else {
        lookup(EVENT_FIX_NAMES, event_id)
    };
    DiagEventInfo { class, name }
}

