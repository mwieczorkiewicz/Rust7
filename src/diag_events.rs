#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn known_mode_transition() {
        let info = describe_event(0x4302);
        assert_eq!(info.class, "Mode transitions");
        assert_eq!(info.name, Some("Mode transition from STARTUP to RUN"));
    }

    #[test]
    fn known_module_diagnostic() {
        let info = describe_event(0x8003);
        assert_eq!(info.class, "Standardized diagnostic data on modules");
        assert_eq!(info.name, Some("Channel error"));
    }

    #[test]
    fn known_user_event() {
        let info = describe_event(0x9002);
        assert_eq!(info.class, "Predefined user events");
        assert_eq!(info.name, Some("OPEN/CLOSED, ON/OFF"));
    }

    #[test]
    fn unknown_class_nibble_returns_unknown_class_and_no_name() {
        // High nibble 0x0 is not in the class table.
        let info = describe_event(0x0000);
        assert_eq!(info.class, "Unknown");
        assert!(info.name.is_none());
    }

    #[test]
    fn known_class_but_unlisted_fix_event_returns_none_name() {
        // 0xA000: class "Freely definable events" — no entries in fix table.
        let info = describe_event(0xA000);
        assert_eq!(info.class, "Freely definable events");
        assert!(info.name.is_none());
    }

    #[test]
    fn known_class_but_unlisted_8x_event_returns_none_name() {
        // 0x8010: class "Standardized diagnostic data on modules" but not catalogued.
        let info = describe_event(0x8010);
        assert_eq!(info.class, "Standardized diagnostic data on modules");
        assert!(info.name.is_none());
    }

    #[test]
    fn fix_table_boundary_entries() {
        // First and last entries in EVENT_FIX_NAMES.
        let first = describe_event(0x113A);
        assert_eq!(
            first.name,
            Some("Start request for cyclic interrupt OB with special handling (S7-300 only)")
        );
        let last = describe_event(0x79E7);
        assert_eq!(last.name, Some("Simulation block (F system block) loaded"));
    }

    #[test]
    fn tables_are_sorted() {
        let sorted = |t: &[(u16, &str)]| t.windows(2).all(|w| w[0].0 < w[1].0);
        assert!(sorted(EVENT_CLASS_NAMES), "EVENT_CLASS_NAMES not sorted");
        assert!(sorted(EVENT_FIX_NAMES), "EVENT_FIX_NAMES not sorted");
        assert!(sorted(EVENT_0X8_0X9_NAMES), "EVENT_0X8_0X9_NAMES not sorted");
    }
}
